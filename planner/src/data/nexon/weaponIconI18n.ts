export function translateWeaponTypeKo(weaponTypeKo: string): string {
  return WEAPON_TYPE_KO_EN[weaponTypeKo] ?? weaponTypeKo
}

export function translateWeaponItemNameKo(itemNameKo: string): string {
  const exact = WEAPON_ITEM_NAME_KO_EN[itemNameKo]
  if (exact) return exact

  const parts = itemNameKo.split(/\s+/).filter(Boolean)
  if (parts.length === 0) return itemNameKo

  const translated = parts.map((part) => WEAPON_TOKEN_KO_EN[part] ?? part)
  if (translated.every((t, i) => t === parts[i])) return itemNameKo
  return translated.join(' ')
}
